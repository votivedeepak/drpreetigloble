    <h3>Procedure for Seeking Information</h3>

    <p>
     Any information, disclosure of which is not prohibited by any law, and information is related to the public affairs of this University, is open to the public and may be obtained from the Public Information Officer (PIO) of this University by submitting written application / request for information.
    </p>

    <p>
      All applications submitted to the University under the RTI Act must specify the full name and address of the applicant, and full particulars of the information required with relevant references, if any. Request for information may be made in Hindi or English. Where the applicant cannot make the request in writing, the assistance of the Public Information Officer may be taken to deduce the oral request into a written application. Requests may be sent by post or personally handed over in the Administrative Office of the University during working hours of the University.
    </p>

    <p>
      All applications must be accompanied by application fee of Rs.10/- and a documentary proof to the effect that applicant is a citizen of India. If volume of information required is large, an additional fee of Rupees two for each additional page in A4 size paper and actual copying cost in larger size paper shall have to be borne by the applicant before requisitioning information. The fee against the application of RTI may be paid by demand draft or banker’s cheque or Indian Postal Order payable to “Dr. Preeti Global University”, payable at Shivpuri, Madhya Pradesh. Persons below poverty line are exempted from payment of application fee and also the fee for providing the information.
    </p>

